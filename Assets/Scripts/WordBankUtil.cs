    // 2-3
    private List<string> simpleWordBank;

    // 4-5 letters or lower
    private List<string> easyWordBank;

    // 6 - 9
    private List<string> mediumWordBank;

    // 10 and above
    private List<string> hardWordBank;

    // longest words unfamiliar in everyday use
